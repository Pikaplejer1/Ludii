package supplementary.experiments.ludemes.LudemeStateAnalysis;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.util.Map;

public class StateRegistryGenerator {

    /**
     * @param registered map: game.name() -> "subPackage.ClassPrefix" (e.g. "t.TicTacToe")
     * @param outputDir same OUTPUT_DIR used by SubclassGenerator (the unsharded root)
     */
    public static void regenerate(Map<String, String> registered, String outputDir) throws IOException {
        StringBuilder sb = new StringBuilder();
        sb.append("package other.state;\n\n");
        sb.append("import java.util.HashMap;\n");
        sb.append("import java.util.Map;\n");
        sb.append("import java.util.function.BiFunction;\n");
        sb.append("import game.Game.StateConstructorLock;\n");
        sb.append("import game.Game;\n\n");
        sb.append("/**\n");
        sb.append(" * AUTO-GENERATED by StateRegistryGenerator. Do not edit by hand.\n");
        sb.append(" */\n");
        sb.append("public final class StateRegistry {\n\n");
        sb.append("    private static final Map<String, BiFunction<Game, StateConstructorLock, State>> FACTORIES = new HashMap<>();\n\n");
        sb.append("    static {\n");
        sb.append("        // BEGIN GENERATED ENTRIES\n");
        for (Map.Entry<String, String> e : registered.entrySet()) {
            // value is e.g. "t.TicTacToe" -> reference as other.state.t.TicTacToeState::new
            sb.append("        FACTORIES.put(\"")
              .append(escape(e.getKey()))
              .append("\", other.state.")
              .append(e.getValue())
              .append("State::new);\n");
        }
        sb.append("        // END GENERATED ENTRIES\n");
        sb.append("    }\n\n");
        sb.append("    private StateRegistry() {}\n\n");
        sb.append("    public static State create(final Game game, final StateConstructorLock lock) {\n");
        sb.append("        final BiFunction<Game, StateConstructorLock, State> factory = FACTORIES.get(game.name());\n");
        sb.append("        if (factory != null) return factory.apply(game, lock);\n");
        sb.append("        return new FullState(game, lock);\n");
        sb.append("    }\n");
        sb.append("}\n");

        Path target = Paths.get(outputDir, "StateRegistry.java");
        Files.writeString(target, sb.toString());
    }

    private static String escape(String s) {
        return s.replace("\\", "\\\\").replace("\"", "\\\"");
    }
}